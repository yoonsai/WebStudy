package com.sist.dao;

public class studentVO {
	private int hakun,kor,eng,math;
	private String name;
	public int getHakun() {
		return hakun;
	}
	public void setHakun(int hakun) {
		this.hakun = hakun;
	}
	public int getKor() {
		return kor;
	}
	public void setKor(int kor) {
		this.kor = kor;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
